/**
 * A2A `TaskStatusUpdateEvent` (spec §4.2.1).
 */
@JsonIgnoreProperties(ignoreUnknown = true)
data class TaskStatusUpdateEvent(
    val taskId: String,
    val contextId: String,
    val status: A2ATaskStatus,
    val final: Boolean = false,
    val kind: String = "status-update",
    @JsonInclude(JsonInclude.Include.NON_NULL)
    val metadata: Map<String, Any?>? = null,
)

/**
 * A2A `TaskArtifactUpdateEvent` (spec §4.2.2).
 */
@JsonIgnoreProperties(ignoreUnknown = true)
data class TaskArtifactUpdateEvent(
    val taskId: String,
    val contextId: String,
    val artifact: A2AArtifact,
    val append: Boolean = false,
    val lastChunk: Boolean = true,
    val kind: String = "artifact-update",
    @JsonInclude(JsonInclude.Include.NON_NULL)
    val metadata: Map<String, Any?>? = null,
)
